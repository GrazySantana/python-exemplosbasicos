# variaveis
nome = "Gisele"
idade = 44


# Exibição de valores
print(nome)
print(idade)


# 1° metodo de concatenação (sting str)
print("O meu nome é: " + nome + "e tenho:" + str (idade) + "anos.")


# 2° Método de concatenação (formal)
print("O meu nome é {} e tenho {} anos.\n".format(nome, idade))


# 3° Método de concatenação (f string)
print(f"O meu nome é: {nome} e tenho {idade} anos.")